package opdrachtComposition.uitleg;

public class Dimensions {

    private int width;
    private int height;
    private int deptl;

    public Dimensions(int width, int height, int deptl) {
        this.width = width;
        this.height = height;
        this.deptl = deptl;
    }


    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public int getDeptl() {
        return deptl;
    }
}
